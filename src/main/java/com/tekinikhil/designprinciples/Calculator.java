package com.tekinikhil.designprinciples;
public class Calculator 
{
	public float Addition(float a,float b)
	{
		return a+b;
	}
	public float Subtraction(float a,float b)
	{
		return a-b;
	}
	public float Multiplication(float a,float b)
	{
		return a*b;
	}
	public float Division(float a,float b)
	{
		return a/b;
	}
	public float Addition(float a,float b,float c)
	{
		return a+b+c;
	}
	public float Subtraction(float a,float b,float c)
	{
		return a-b-c;
	}
	public float Multiplication(float a,float b,float c)
	{
		return a*b*c;
	}
	public float Division(float a,float b,float c)
	{
		return (a/b)/c;
	}
}
